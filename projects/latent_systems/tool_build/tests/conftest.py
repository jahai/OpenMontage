"""Per-test tmp_path-isolated state.db fixture.

Eliminates Pattern #8 reinforcement #3 (test cleanup leak class — production
tables polluted by test runs from derivative IDs generated by production
code that the test's prefix-based cleanup() doesn't track). Banking
principle #7: enforce test isolation at the fixture level, not via cleanup
discipline.

Phase 2.5 promotion (banked 2026-05-09): MUST-DO before Wave 1 code starts
— every new test added under the cleanup-discipline pattern would extend
the leak surface.

Design (per SESSION_STARTER.md Day 1 directive 2026-05-13):
- Session-scoped template DB: `alembic upgrade head` runs once per test
  session against a tmp_path_factory location. Saves ~15-45s vs per-test
  migration on a 15-test suite.
- Function-scoped autouse `isolated_db`: copies the template, creates
  `_data/` artifact subdirs under tmp_path, monkey-patches db module
  globals + 5 dependent modules that snapshot `db.DATA_DIR` at import
  time as module-level constants (runtime, router_tail, retry_queue,
  thumbnails, watcher).
- Tests keep existing main()/cleanup() bookends. Each test file adds
  `def test_main(): assert main() == 0` for pytest discovery; the autouse
  fixture wraps it. Standalone `python tests/test_X.py` still works.

This conftest depends on the `TOOL_BUILD_ALEMBIC_URL` env var override
added to migrations/env.py 2026-05-13.
"""

